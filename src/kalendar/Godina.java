package kalendar;

public class Godina {
	
	private int godina = 0;
	private int januar = 0;
	private int februar = 0;
	private int mart = 0;
	private int april = 0;
	private int maj = 0;
	private int jun = 0;
	private int jul = 0;
	private int avgust = 0;
	private int septembar = 0;
	private int oktobar = 0;
	private int novembar = 0;
	private int decembar = 0;
	
	Godina(int newGodina, int newJanuar, int newFebruar, int newMart, int newApril, int newMaj, int newJun, int newJul, int newAvgust, int newSeptembar, int newOktobar, int newNovembar, int newDecembar){
		godina = newGodina;
		januar = newJanuar;
		februar = newFebruar;
		mart = newMart;
		april = newApril;
		maj = newMaj;
		jun = newJun;
		jul = newJul;
		avgust = newAvgust;
		septembar = newSeptembar;
		oktobar = newOktobar;
		novembar = newNovembar;
		decembar = newDecembar;
	}
	
	public int getGodina() {
		return godina;
	}
	public void setGodina(int godina) {
		this.godina = godina;
	}
	public int getJanuar() {
		return januar;
	}
	public void setJanuar(int januar) {
		this.januar = januar;
	}
	public int getFebruar() {
		return februar;
	}
	public void setFebruar(int februar) {
		this.februar = februar;
	}
	public int getMart() {
		return mart;
	}
	public void setMart(int mart) {
		this.mart = mart;
	}
	public int getApril() {
		return april;
	}
	public void setApril(int april) {
		this.april = april;
	}
	public int getMaj() {
		return maj;
	}
	public void setMaj(int maj) {
		this.maj = maj;
	}
	public int getJun() {
		return jun;
	}
	public void setJun(int jun) {
		this.jun = jun;
	}
	public int getJul() {
		return jul;
	}
	public void setJul(int jul) {
		this.jul = jul;
	}
	public int getAvgust() {
		return avgust;
	}
	public void setAvgust(int avgust) {
		this.avgust = avgust;
	}
	public int getSeptembar() {
		return septembar;
	}
	public void setSeptembar(int septembar) {
		this.septembar = septembar;
	}
	public int getOktobar() {
		return oktobar;
	}
	public void setOktobar(int oktobar) {
		this.oktobar = oktobar;
	}
	public int getNovembar() {
		return novembar;
	}
	public void setNovembar(int novembar) {
		this.novembar = novembar;
	}
	public int getDecembar() {
		return decembar;
	}
	public void setDecembar(int decembar) {
		this.decembar = decembar;
	}
	
	
	
}
